"""
В пиццерии хранится информация о том, кто, что и сколько заказывал у них в определённый период. Нужно структурировать эту информацию, а также понять сколько всего пицц купил каждый заказчик. На вход в программу подаётся N заказов. Каждый заказ представляет собой строку вида Покупатель - название пиццы - количество заказанных пицц. Реализуйте код, который выводит список покупателей и их заказов по алфавиту. Учитывайте, что один человек может заказать одно и то же несколько раз (2 балла)

#### Пример 1:
```
Введите кол-во заказов: 6
1 заказ: Иванов Пепперони 1
2 заказ: Петров Де-Люкс 2
3 заказ: Иванов Мясная 3
4 заказ: Иванов Мексиканская 2
5 заказ: Иванов Пепперони 2
6 заказ: Петров Интересная 5

Иванов:
	Мексиканская: 2
	Мясная: 3
	Пепперони: 3
Петров:
	Де-Люкс: 2
	Интересная: 5
```
"""
n = int(input("Введите кол-во заказов: "))
orders = {}

for i in range(n):
    data = input(f"{i+1} заказ: ").split()
    if len(data) < 3:
        continue
        
    customer = data[0]
    pizza = ' '.join(data[1:-1])
    count = int(data[-1])
    
    if customer not in orders:
        orders[customer] = {}
    if pizza not in orders[customer]:
        orders[customer][pizza] = 0
    orders[customer][pizza] += count

for customer in sorted(orders.keys()):
    print(f"{customer}:")
    for pizza in sorted(orders[customer].keys()):
        print(f"    {pizza}: {orders[customer][pizza]}")
